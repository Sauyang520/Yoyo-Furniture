    /**
     * Validate the information format
     * @param name name
     * @param phone phone
     * @param email email
     * @param password password
     * @return true if all valid, vice versa
     */
    private boolean validateInformation(String name, String phone, String email, String password) {
        if (name.isEmpty()) {
            jLabelWarning.setText("Please enter proper name");
        } else if (!HandleAccount.validatePhone(phone)) {
            jLabelWarning.setText("Please enter valid Malaysia phone number");
        } else if (!HandleAccount.validateEmail(email)) {
            jLabelWarning.setText("Please enter valid email");
        } else if (!HandleAccount.validateNewPassword(password)) {
            jLabelWarning.setText("The new password must be at least 8 characters with 1 Capital Letter and 1 symbol");
        } else if (!HandleAccount.comparePassword(password, String.valueOf(jPasswordFieldRe.getPassword()))) {
            jLabelWarning.setText("Re-enter password does not match new password");
        } else {
            return true; // Validation passed
        }

        return false; // Validation failed
    }

