    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        String id = newID[ID.OFFICER_ID.getIndex()];
        String name = jTextFieldName.getText();
        String password = String.valueOf(jPasswordFieldPass.getPassword());
        String phone = jTextFieldPhone.getText();
        String email = jTextFieldEmail.getText();


        if (!validateInformation(name, phone, email, password)){
            return;
        }

        String[] newUser = {id, HandleAccount.hashString(password), name, phone, email};
        String user = String.join(",", newUser);

        try {
            Administrator.addOfc(user, newID);
        } catch (Exception e) {
            e.printStackTrace();
        }

        JOptionPane.showMessageDialog(this, "Officer account created successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        try {
            newID = HandleAccount.generateNewID(ID.OFFICER_ID);
            jTextFieldId.setText(newID[ID.OFFICER_ID.getIndex()]);
        } catch (Exception e) {
            e.printStackTrace();
        }

        jButtonClearActionPerformed(evt);
    }//GEN-LAST:event_jButtonAddActionPerformed

