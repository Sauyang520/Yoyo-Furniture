    private boolean validateOrderStatus(SaleOrder saleOrder){
        return !saleOrder.getStatus().equals(Status.PENDING.getStatus()) &&
           !saleOrder.getStatus().equals(Status.UNAPPROVED.getStatus());
    }
        
    

