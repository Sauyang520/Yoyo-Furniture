    private void jGenerateClosedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGenerateClosedActionPerformed
        String selectedMonth = (String) jComboBoxMonth1.getSelectedItem();
        String selectedYear = (String) jComboBoxYear1.getSelectedItem();
        SaleReport salereport = new SaleReport();
        
        try {
            salereport.generateClosedSaleReport(officer.getUserID(),selectedMonth,selectedYear);
                int confirmation = JOptionPane.showOptionDialog(this,"ClosedSales Report Successfully created!","Message",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE,null,new String[]{"Open in Folder", "Close"},"Close");
        
                    if (confirmation == JOptionPane.YES_OPTION) {
                         String folderPath = "src/main/resources/com.yoyofurniture.closedsalereport";
                        File folder = new File(folderPath);

                        if (Desktop.isDesktopSupported()) {
                            Desktop desktop = Desktop.getDesktop();
                            try {
                                desktop.open(folder);
                            } catch (IOException ex) {
                                ex.printStackTrace();
                            }
                        }
                        ((JDialog) SwingUtilities.getRoot((Component) evt.getSource())).dispose();
                    } else {
                        ((JDialog) SwingUtilities.getRoot((Component) evt.getSource())).dispose();
                    }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(OfcGenerateReport.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jGenerateClosedActionPerformed

