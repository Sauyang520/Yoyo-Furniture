    private void jGenerateInvoiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGenerateInvoiceActionPerformed
        try {
            String selectedSaleOrderID = (String) jComboBoxInvoice.getSelectedItem();
            SaleOrder selectedsaleOrder = HandleSales.getSaleOrderByID(selectedSaleOrderID);
            
            if(validateOrderStatus(selectedsaleOrder)){
                jInvoiceError.setText("");
                Invoice invoice = new Invoice(selectedsaleOrder);
                try {
                    salesperson = HandleAccount.readAllData("salesperson.txt", userinfo -> new Salesperson(userinfo[0], userinfo[1],
                            userinfo[2], userinfo[3], userinfo[4], userinfo[5], userinfo[6]), ","
                    ).stream().filter(user -> user.getUserID().equals(selectedsaleOrder.getSalesperson())).findFirst()
                            .map(user -> (Salesperson)user).get();
                    customer = HandleAccount.readAllData("customer.txt", userinfo -> new Customer(userinfo[0], userinfo[1],
                            userinfo[2], userinfo[3]), "\\|"
                    ).stream().filter(user -> user.getUserID().equals(selectedsaleOrder.getCustomer())).findFirst()
                            .map(user -> (Customer)user).get();
                } catch (Exception e) {
                    throw new RuntimeException(e);
                }
                try {
                    invoice.generateInvoice(salesperson,customer);
                        int confirmation = JOptionPane.showOptionDialog(this,"Invoice Successfully created!","Message",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE,null,new String[]{"Open in Folder", "Close"},"Close");
        
                    if (confirmation == JOptionPane.YES_OPTION) {
                         String folderPath = "src/main/resources/com.yoyofurniture.invoice";
                        File folder = new File(folderPath);

                        if (Desktop.isDesktopSupported()) {
                            Desktop desktop = Desktop.getDesktop();
                            try {
                                desktop.open(folder);
                            } catch (IOException ex) {
                                ex.printStackTrace();
                            }
                        }
                        ((JDialog) SwingUtilities.getRoot((Component) evt.getSource())).dispose();
                    } else {
                        ((JDialog) SwingUtilities.getRoot((Component) evt.getSource())).dispose();
                    }
                    
                } catch (FileNotFoundException e) {
                    throw new RuntimeException(e);
                }
            } else {
                jInvoiceError.setText("Sale Order not approved yet.");
                
            }
        } catch (Exception ex) {
            Logger.getLogger(OfcGenerateReport.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jGenerateInvoiceActionPerformed

